"use client"

import { useState, useRef, useEffect } from "react"
import { Button } from "@/components/ui/button"
import { Input } from "@/components/ui/input"
import { Avatar, AvatarFallback } from "@/components/ui/avatar"
import { Badge } from "@/components/ui/badge"
import { Card, CardContent } from "@/components/ui/card"
import { Separator } from "@/components/ui/separator"
import {
  Send,
  X,
  Sparkles,
  Command,
  FileText,
  BarChart3,
  Settings,
  GripVertical,
  Minimize2,
  Maximize2,
  Globe,
  Zap,
  HelpCircle,
  Code,
} from "lucide-react"

interface Message {
  id: string
  role: "user" | "assistant"
  content: string
}

const suggestedPrompts = [
  { icon: Zap, label: "Summarize this page" },
  { icon: HelpCircle, label: "Explain this data" },
  { icon: Code, label: "Generate a report" },
]

export default function CopilotSidebarStandard() {
  const [messages, setMessages] = useState<Message[]>([
    {
      id: "1",
      role: "assistant",
      content:
        "Hi! I'm your AI copilot. I can see you're on the Dashboard page. How can I help you with your data?",
    },
    {
      id: "2",
      role: "user",
      content: "What are the key metrics I should focus on today?",
    },
    {
      id: "3",
      role: "assistant",
      content:
        "Based on your current dashboard context, here are the key areas to focus on:\n\n1. Revenue is up 12% - great momentum to maintain\n2. You have 2 unresolved support tickets that are overdue\n3. User signups have slowed compared to last week\n\nWould you like me to draft an action plan for any of these?",
    },
  ])
  const [input, setInput] = useState("")
  const [sidebarOpen, setSidebarOpen] = useState(true)
  const [isMinimized, setIsMinimized] = useState(false)
  const [panelWidth, setPanelWidth] = useState(360)
  const [isDesktop, setIsDesktop] = useState(false)
  const scrollRef = useRef<HTMLDivElement>(null)
  const isResizing = useRef(false)

  useEffect(() => {
    const mq = window.matchMedia("(min-width: 640px)")
    setIsDesktop(mq.matches)
    const handler = (e: MediaQueryListEvent) => setIsDesktop(e.matches)
    mq.addEventListener("change", handler)
    return () => mq.removeEventListener("change", handler)
  }, [])

  useEffect(() => {
    scrollRef.current?.scrollTo({ top: scrollRef.current.scrollHeight })
  }, [messages])

  const handleSend = (content?: string) => {
    const text = content || input.trim()
    if (!text) return
    const userMsg: Message = {
      id: Date.now().toString(),
      role: "user",
      content: text,
    }
    setMessages((prev) => [
      ...prev,
      userMsg,
      {
        id: (Date.now() + 1).toString(),
        role: "assistant",
        content:
          "I've analyzed your request in the context of the current page. In production, this response would be generated by your AI backend with full access to the page context and user data.",
      },
    ])
    setInput("")
  }

  const handleMouseDown = () => {
    isResizing.current = true
    const handleMouseMove = (e: MouseEvent) => {
      if (!isResizing.current) return
      const newWidth = window.innerWidth - e.clientX
      setPanelWidth(Math.max(280, Math.min(500, newWidth)))
    }
    const handleMouseUp = () => {
      isResizing.current = false
      document.removeEventListener("mousemove", handleMouseMove)
      document.removeEventListener("mouseup", handleMouseUp)
    }
    document.addEventListener("mousemove", handleMouseMove)
    document.addEventListener("mouseup", handleMouseUp)
  }

  // Minimized floating button
  if (isMinimized) {
    return (
      <div className="mx-auto flex h-[calc(100vh-3rem)] min-h-[500px] w-full max-w-4xl flex-col overflow-hidden sm:flex-row">
        <div className="flex flex-1 flex-col">
          <div className="flex items-center justify-between border-b px-6 py-3">
            <h2 className="font-semibold">Dashboard</h2>
          </div>
          <div className="relative flex-1 overflow-y-auto p-6">
            <div className="space-y-6">
              <div>
                <h3 className="mb-3 text-lg font-semibold">Overview</h3>
                <div className="grid min-w-0 grid-cols-1 gap-4 sm:grid-cols-3">
                  <Card className="gap-2 overflow-hidden py-3">
                    <CardContent className="px-4 py-0">
                      <div className="flex items-center gap-2 text-sm text-muted-foreground">
                        <BarChart3 className="h-4 w-4 shrink-0" />
                        <span className="truncate">Revenue</span>
                      </div>
                      <p className="mt-1 truncate text-xl font-bold sm:text-2xl">$12,480</p>
                    </CardContent>
                  </Card>
                  <Card className="gap-2 overflow-hidden py-3">
                    <CardContent className="px-4 py-0">
                      <div className="flex items-center gap-2 text-sm text-muted-foreground">
                        <FileText className="h-4 w-4 shrink-0" />
                        <span className="truncate">Documents</span>
                      </div>
                      <p className="mt-1 truncate text-xl font-bold sm:text-2xl">284</p>
                    </CardContent>
                  </Card>
                  <Card className="gap-2 overflow-hidden py-3">
                    <CardContent className="px-4 py-0">
                      <div className="flex items-center gap-2 text-sm text-muted-foreground">
                        <Settings className="h-4 w-4 shrink-0" />
                        <span className="truncate">Active Users</span>
                      </div>
                      <p className="mt-1 truncate text-xl font-bold sm:text-2xl">1,429</p>
                    </CardContent>
                  </Card>
                </div>
              </div>
            </div>
            {/* Floating button */}
            <button
              onClick={() => setIsMinimized(false)}
              className="absolute bottom-4 right-4 flex h-12 w-12 items-center justify-center rounded-full bg-primary text-primary-foreground shadow-lg transition-transform hover:scale-105"
            >
              <Sparkles className="h-5 w-5" />
            </button>
          </div>
        </div>
      </div>
    )
  }

  return (
    <div className="mx-auto flex h-[calc(100vh-3rem)] min-h-[500px] w-full max-w-4xl flex-col overflow-hidden sm:flex-row">
      {/* Main content area */}
      <div className="flex min-h-0 flex-1 flex-col overflow-hidden">
        <div className="flex items-center justify-between border-b px-6 py-3">
          <h2 className="font-semibold">Dashboard</h2>
          {!sidebarOpen && (
            <Button
              variant="outline"
              size="sm"
              onClick={() => setSidebarOpen(true)}
              className="gap-2"
            >
              <Sparkles className="h-4 w-4" />
              AI Copilot
            </Button>
          )}
        </div>
        <div className="flex-1 overflow-y-auto p-6">
          <div className="space-y-6">
            <div>
              <h3 className="mb-3 text-lg font-semibold">Overview</h3>
              <div className="grid min-w-0 grid-cols-1 gap-4 sm:grid-cols-3">
                <Card className="gap-2 overflow-hidden py-3">
                  <CardContent className="px-4 py-0">
                    <div className="flex items-center gap-2 text-sm text-muted-foreground">
                      <BarChart3 className="h-4 w-4 shrink-0" />
                      <span className="truncate">Revenue</span>
                    </div>
                    <p className="mt-1 truncate text-xl font-bold sm:text-2xl">$12,480</p>
                    <p className="truncate text-xs text-muted-foreground">+12% today</p>
                  </CardContent>
                </Card>
                <Card className="gap-2 overflow-hidden py-3">
                  <CardContent className="px-4 py-0">
                    <div className="flex items-center gap-2 text-sm text-muted-foreground">
                      <FileText className="h-4 w-4 shrink-0" />
                      <span className="truncate">Documents</span>
                    </div>
                    <p className="mt-1 truncate text-xl font-bold sm:text-2xl">284</p>
                    <p className="truncate text-xs text-muted-foreground">8 new</p>
                  </CardContent>
                </Card>
                <Card className="gap-2 overflow-hidden py-3">
                  <CardContent className="px-4 py-0">
                    <div className="flex items-center gap-2 text-sm text-muted-foreground">
                      <Settings className="h-4 w-4 shrink-0" />
                      <span className="truncate">Active Users</span>
                    </div>
                    <p className="mt-1 truncate text-xl font-bold sm:text-2xl">1,429</p>
                    <p className="truncate text-xs text-muted-foreground">+3.2%</p>
                  </CardContent>
                </Card>
              </div>
            </div>
            <div>
              <h3 className="mb-3 text-sm font-semibold">Recent Activity</h3>
              <div className="space-y-2">
                {[
                  "New user signed up: alex@example.com",
                  "Invoice #1084 was paid ($299)",
                  "Support ticket #342 resolved",
                  "Deployment v2.4.1 completed",
                ].map((item, i) => (
                  <div
                    key={i}
                    className="rounded-md border px-3 py-2 text-sm text-muted-foreground"
                  >
                    {item}
                  </div>
                ))}
              </div>
            </div>
          </div>
        </div>
      </div>

      {/* Copilot sidebar panel */}
      {sidebarOpen && (
        <div
          className="flex max-h-[50%] min-h-0 w-full flex-col overflow-hidden border-t bg-background sm:max-h-none sm:w-auto sm:shrink-0 sm:border-l sm:border-t-0"
          style={isDesktop ? { width: panelWidth } : undefined}
        >
          {/* Resize handle */}
          <div
            className="absolute left-0 top-0 z-10 flex h-full w-1 cursor-col-resize items-center justify-center hover:bg-primary/20"
            onMouseDown={handleMouseDown}
          >
            <GripVertical className="h-4 w-4 text-muted-foreground opacity-0 transition-opacity hover:opacity-100" />
          </div>

          {/* Header */}
          <div className="flex items-center justify-between border-b px-3 py-2.5">
            <div className="flex items-center gap-2">
              <Sparkles className="h-4 w-4" />
              <h3 className="text-sm font-semibold">AI Copilot</h3>
            </div>
            <div className="flex items-center gap-0.5">
              <Button
                variant="ghost"
                size="icon"
                className="h-7 w-7"
                onClick={() => setIsMinimized(true)}
              >
                <Minimize2 className="h-3.5 w-3.5" />
              </Button>
              <Button
                variant="ghost"
                size="icon"
                className="h-7 w-7"
                onClick={() => setSidebarOpen(false)}
              >
                <X className="h-3.5 w-3.5" />
              </Button>
            </div>
          </div>

          {/* Context indicator */}
          <div className="flex items-center gap-2 border-b bg-muted/30 px-3 py-2">
            <Globe className="h-3 w-3 text-muted-foreground" />
            <span className="text-[11px] text-muted-foreground">
              Viewing:
            </span>
            <Badge variant="secondary" className="h-5 text-[10px]">
              Dashboard / Overview
            </Badge>
          </div>

          {/* Width indicator */}
          <div className="flex items-center justify-center border-b py-1">
            <span className="text-[10px] text-muted-foreground">
              {panelWidth}px
            </span>
          </div>

          {/* Messages */}
          <div ref={scrollRef} className="flex-1 overflow-y-auto p-3">
            <div className="space-y-3">
              {messages.map((msg) => (
                <div
                  key={msg.id}
                  className={`flex gap-2 ${msg.role === "user" ? "flex-row-reverse" : ""}`}
                >
                  <Avatar className="h-6 w-6 shrink-0">
                    <AvatarFallback className="text-[10px]">
                      {msg.role === "user" ? "U" : "AI"}
                    </AvatarFallback>
                  </Avatar>
                  <div
                    className={`max-w-[85%] rounded-lg px-3 py-2 text-xs leading-relaxed ${
                      msg.role === "user"
                        ? "bg-primary text-primary-foreground"
                        : "bg-muted"
                    }`}
                  >
                    <div className="whitespace-pre-wrap">{msg.content}</div>
                  </div>
                </div>
              ))}
            </div>
          </div>

          {/* Suggested prompts */}
          <div className="border-t px-3 py-2">
            <p className="mb-1.5 text-[10px] font-medium text-muted-foreground">
              Suggested
            </p>
            <div className="flex flex-wrap gap-1.5">
              {suggestedPrompts.map((prompt) => (
                <button
                  key={prompt.label}
                  onClick={() => handleSend(prompt.label)}
                  className="flex items-center gap-1 rounded-full border bg-background px-2.5 py-1 text-[11px] text-muted-foreground transition-colors hover:bg-muted hover:text-foreground"
                >
                  <prompt.icon className="h-3 w-3" />
                  {prompt.label}
                </button>
              ))}
            </div>
          </div>

          <Separator />

          {/* Input */}
          <div className="p-3">
            <form
              onSubmit={(e) => {
                e.preventDefault()
                handleSend()
              }}
              className="flex gap-2"
            >
              <Input
                value={input}
                onChange={(e) => setInput(e.target.value)}
                placeholder="Ask about this page..."
                className="h-8 flex-1 text-xs"
              />
              <Button
                type="submit"
                size="icon"
                className="h-8 w-8"
                disabled={!input.trim()}
              >
                <Send className="h-3.5 w-3.5" />
              </Button>
            </form>
            <div className="mt-2 flex items-center justify-center gap-1 text-[10px] text-muted-foreground">
              <Command className="h-3 w-3" />
              <span>K to toggle copilot</span>
            </div>
          </div>
        </div>
      )}
    </div>
  )
}
